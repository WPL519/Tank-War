import com.tank.TankFrame;

import java.awt.*;

public class Main {

    public static void main(String[] args) throws Exception {
        TankFrame tf = new TankFrame();
        //在主线程每隔50毫秒刷新窗口，调用repaint方法重画
        while (true){
            Thread.sleep(50);
            tf.repaint();
        }

    }
}
